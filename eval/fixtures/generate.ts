#!/usr/bin/env tsx
/**
 * Render the starter specimen set to PNGs via Playwright and write the matching
 * manifest.json. Ground-truth labels are exact because we control what's rendered.
 *
 * Usage: pnpm eval:fixtures
 *
 * Output:
 *   eval/images/<id>.png           (gitignored)
 *   eval/manifest.json             (committed)
 *
 * This runs in the dev environment only. The real-image eval (pnpm eval) reads
 * whatever manifest.json says — you can add your own hand-labeled cases alongside
 * the generated ones.
 */
import { writeFile, mkdir } from "node:fs/promises";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { chromium } from "@playwright/test";
import { SPECIMENS, type SpecimenSpec } from "./specs";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "../..");
const imagesDir = resolve(repoRoot, "eval/images");
const manifestPath = resolve(repoRoot, "eval/manifest.json");

const VIEWPORT = { width: 1280, height: 800 };

function buildHtml(spec: SpecimenSpec): string {
  const families = [
    ...new Set(spec.blocks.map((b) => `${b.family}:wght@${b.weight}${b.italic ? "i" : ""}`)),
  ];
  const fontsParam = families
    .map((f) => {
      // Google Fonts css2 needs the name in a specific shape. For italic we need ital,wght@0,400;1,400 style.
      const [family, rest] = f.split(":wght@");
      const weight = rest?.replace("i", "") ?? "400";
      const italic = rest?.endsWith("i") ? "1" : "0";
      return `family=${family!.replace(/ /g, "+")}:ital,wght@${italic},${weight}`;
    })
    .join("&");

  const background = spec.background ?? "#ffffff";

  const blocks = spec.blocks
    .map((b) => {
      const style = [
        `font-family: '${b.family}', system-ui, sans-serif`,
        `font-weight: ${b.weight}`,
        b.italic ? `font-style: italic` : "",
        `font-size: ${b.sizePx}px`,
        `line-height: 1.1`,
        `color: ${b.color ?? "#111111"}`,
        b.letterSpacingEm ? `letter-spacing: ${b.letterSpacingEm}em` : "",
        b.uppercase ? `text-transform: uppercase` : "",
        `margin: 0 0 24px 0`,
        `max-width: 72ch`,
        `white-space: pre-wrap`,
      ]
        .filter(Boolean)
        .join(";");
      return `<div style="${style}">${escapeHtml(b.text)}</div>`;
    })
    .join("\n");

  return `<!doctype html>
<html lang="en">
  <head>
    <meta charset="utf-8" />
    <link rel="preconnect" href="https://fonts.googleapis.com" />
    <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin />
    <link href="https://fonts.googleapis.com/css2?${fontsParam}&display=block" rel="stylesheet" />
    <style>
      html, body { margin: 0; padding: 0; }
      body {
        background: ${background};
        padding: 72px 80px;
        min-height: 100vh;
        box-sizing: border-box;
      }
    </style>
  </head>
  <body>
    ${blocks}
  </body>
</html>`;
}

function escapeHtml(s: string): string {
  return s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}

async function main(): Promise<void> {
  await mkdir(imagesDir, { recursive: true });

  console.log(`Launching headless Chromium to render ${SPECIMENS.length} specimen(s)…`);
  const browser = await chromium.launch();
  try {
    const page = await browser.newPage({ viewport: VIEWPORT });
    for (const spec of SPECIMENS) {
      process.stdout.write(`  ${spec.id.padEnd(44)} `);
      const html = buildHtml(spec);
      await page.setContent(html, { waitUntil: "domcontentloaded" });
      // Wait for all @font-face loads to complete before screenshotting.
      await page.evaluate(async () => {
        await document.fonts.ready;
        // tiny extra tick so the layout settles with final metrics
        await new Promise((r) => setTimeout(r, 50));
      });
      const outPath = resolve(imagesDir, `${spec.id}.png`);
      await page.screenshot({ path: outPath, type: "png", fullPage: false });
      console.log(`→ eval/images/${spec.id}.png`);
    }
  } finally {
    await browser.close();
  }

  const manifest = {
    version: 1 as const,
    cases: SPECIMENS.map((spec) => ({
      id: spec.id,
      image: `${spec.id}.png`,
      category: spec.category,
      labels: spec.blocks.map((b) => ({
        region: b.region,
        expected: [b.family],
      })),
      source: "generated by eval/fixtures/generate.ts",
      notes: `Rendered specimen: ${spec.blocks.map((b) => b.family).join(" + ")}`,
    })),
  };
  await writeFile(manifestPath, JSON.stringify(manifest, null, 2) + "\n");
  console.log(`\nWrote ${manifestPath} (${manifest.cases.length} cases).`);
  console.log(`Run \`pnpm eval\` to score the model against these specimens.`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
